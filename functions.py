def square(side): # Define the square function.
    area = side * side # Calculate the area of a square.
    print ("The area of the square is " + str(area) + " square units. ")

def circle(radius): # define the circle function.
    area = 3.14 * radius * radius #calculate the area of a circle. 
    print ("The area of a circle is " + str(area) + " square units. ")

# Test the functions.
square (4)
circle (5)
